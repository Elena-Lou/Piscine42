#include "ft_header.h"

void	ft_print_solve(int **tab_check)
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			ft_putchar(tab_check[i][j]);
			j++;
		}
		i++;
	}
}
