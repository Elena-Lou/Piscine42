#include <unistd.h>

void	ft_putstr(char *s1)
{
	int	i;

	i = 0;
	while (s1[i] == ' ' || (s1[i] >= '0' && s1[i] <= '9') || s1[i] == ':')
		i++;
	while (s1[i])
	{
		write(1, &s1[i], 1);
		i++;
	}
}
